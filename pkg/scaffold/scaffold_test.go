func assertAllStepsOK(t *testing.T, res scaffold.Result) {
	t.Helper()
	require.NotEmpty(t, res.Steps)
	for _, s := range res.Steps {
		assert.Truef(t, s.OK, "step %s failed: %s", s.Name, s.Message)
	}
}

func assertBuilds(t *testing.T, dir string) {
	t.Helper()
	cmd := exec.CommandContext(context.Background(), "go", "build", "./...")
	cmd.Dir = dir
	out, err := cmd.CombinedOutput()
	require.NoErrorf(t, err, "go build ./... failed in %s:\n%s", dir, out)
}

// requireNetwork skips a test that needs to `go install`/`go get` a tool not
// already in the module cache, when explicitly told there's no network
// (CONDUIT_TEST_NO_NETWORK=1) — this repository's CI has network access, so
// this is an escape hatch for local/offline runs, not the default.
func requireNetwork(t *testing.T) {
	t.Helper()
	if os.Getenv("CONDUIT_TEST_NO_NETWORK") != "" {
		t.Skip("CONDUIT_TEST_NO_NETWORK set")
	}
}
