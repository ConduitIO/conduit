// TestGenerate_Connector is this command's headline acceptance criterion:
// `connector new --lang go s3 --yes` scaffolds ./conduit-connector-s3 that
// `go build ./...` compiles with no edits. It also asserts the
// connector-specific generate step actually ran (connector.yaml gets
// rewritten by conn-sdk-cli specgen, so its "name:" field should reflect
// the scaffolded name).
func TestGenerate_Connector(t *testing.T) {
	requireNetwork(t)

	dir := filepath.Join(t.TempDir(), "conduit-connector-s3")
	res, err := scaffold.Generate(context.Background(), scaffold.Request{
		Kind:     scaffold.KindConnector,
		Language: scaffold.LanguageGo,
		Name:     "s3",
		Module:   "github.com/devaris/conduit-connector-s3",
		Path:     dir,
		Git:      true,
	})
	require.NoError(t, err)

	assert.Equal(t, dir, res.Path)
	assert.Equal(t, scaffold.KindConnector, res.Kind)
	assert.NotEmpty(t, res.TemplateRef)
	assertAllStepsOK(t, res)

	assertBuilds(t, dir)
	assert.DirExists(t, filepath.Join(dir, ".git"))

	yaml, err := os.ReadFile(filepath.Join(dir, "connector.yaml"))
	require.NoError(t, err)
	assert.Contains(t, string(yaml), "name: s3", "connector.yaml should reflect a fresh conn-sdk-cli specgen run, not just the pre-committed placeholder")
}

// TestGenerate_Processor is the processor half of the same acceptance
// criterion, plus the must-fix from the design review: the processor path
// must use paramgen, not conn-sdk-cli specgen — asserted here by checking
// paramgen_proc.go's generated header and package name.
func TestGenerate_Processor(t *testing.T) {
	requireNetwork(t)

	dir := filepath.Join(t.TempDir(), "conduit-processor-uppercase")
	res, err := scaffold.Generate(context.Background(), scaffold.Request{
		Kind:     scaffold.KindProcessor,
		Language: scaffold.LanguageGo,
		Name:     "uppercase",
		Module:   "github.com/devaris/conduit-processor-uppercase",
		Path:     dir,
		Git:      true,
	})
	require.NoError(t, err)

	assert.Equal(t, dir, res.Path)
	assert.Equal(t, scaffold.KindProcessor, res.Kind)
	assertAllStepsOK(t, res)

	assertBuilds(t, dir)

	pg, err := os.ReadFile(filepath.Join(dir, "paramgen_proc.go"))
	require.NoError(t, err)
	assert.Contains(t, string(pg), "Code generated by paramgen", "processor generate step must use paramgen, not conn-sdk-cli specgen")
	assert.Contains(t, string(pg), "package uppercase")
}

func TestGenerate_SkipGenerate_StillBuilds(t *testing.T) {
	dir := filepath.Join(t.TempDir(), "conduit-connector-s3")
	res, err := scaffold.Generate(context.Background(), scaffold.Request{
		Kind:         scaffold.KindConnector,
		Language:     scaffold.LanguageGo,
		Name:         "s3",
		Module:       "github.com/devaris/conduit-connector-s3",
		Path:         dir,
		Git:          false,
		SkipGenerate: true,
	})
	require.NoError(t, err)
	for _, s := range res.Steps {
		assert.NotEqual(t, "install_tools", s.Name)
		assert.NotEqual(t, "generate", s.Name)
	}
	assertBuilds(t, dir)
	assert.NoDirExists(t, filepath.Join(dir, ".git"))
}

func TestGenerate_ValidationFailure_NoWrite(t *testing.T) {
	dir := filepath.Join(t.TempDir(), "conduit-connector-bad")
	_, err := scaffold.Generate(context.Background(), scaffold.Request{
		Kind:     scaffold.KindConnector,
		Language: scaffold.LanguageGo,
		Name:     "s3",
		Module:   "not-a-valid-module",
		Path:     dir,
	})
	require.Error(t, err)
	ce, ok := conduiterr.Get(err)
	require.True(t, ok)
	assert.Equal(t, scaffold.CodeInvalidModule.Reason(), ce.Code.Reason())
	assert.Equal(t, exitcode.Validation, exitcode.ExitCode(err))
	assert.NoDirExists(t, dir, "a validation failure must not write anything")
}

