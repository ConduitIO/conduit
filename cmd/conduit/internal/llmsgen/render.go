// generatedHeader is the "do not edit by hand" marker every generated Go
// file in this repo carries in spirit (e.g. pkg/pipeline/status_string.go),
// adapted to a text file: an HTML comment renders invisibly wherever
// llms.txt is viewed as Markdown, but is still plainly visible as plain
// text.
const generatedHeader = "<!-- Code generated by cmd/conduit/internal/llmsgen; DO NOT EDIT. -->"

// summary is the one piece of hand-curated prose in either generated file
// (design doc's "File structure" section) — everything else is derived
// from the four sources.
const summary = "Broker-neutral data streaming engine and Kafka Connect replacement. Pipelines move " +
	"records from source connectors to destination connectors with processors in between; " +
	"any-language plugins over gRPC/WASM; embeddable; agent-legible with stable error codes and an " +
	"MCP server."

// generatedData bundles every source the generator reads, already gathered
// and sorted, so render{LLMS,LLMSFull}Txt are pure functions over plain
// data — the "formatter fixtures" golden tests in llmsgen_test.go feed
// these functions synthetic data without touching the real registries.
type generatedData struct {
	Connectors []connectorInfo
	ErrorCodes []errorCodeInfo
	MCPTools   []mcp.ToolInfo
	Config     configSchema
}

// readTools and writeTools split d.MCPTools by Mutates, each preserving
// the already-sorted-by-Name order of d.MCPTools.
func (d generatedData) readTools() []mcp.ToolInfo  { return filterTools(d.MCPTools, false) }
func (d generatedData) writeTools() []mcp.ToolInfo { return filterTools(d.MCPTools, true) }

func filterTools(tools []mcp.ToolInfo, mutates bool) []mcp.ToolInfo {
	var out []mcp.ToolInfo
	for _, t := range tools {
		if t.Mutates == mutates {
			out = append(out, t)
		}
	}
	return out
}

// renderLLMSTxt renders the concise, llmstxt.org-conformant index: one H1,
// one blockquote summary immediately after, then ## sections of links and
// counts. Every list here is derived from d; the full detail lives in
// llms-full.txt, which every section links to.
func renderLLMSTxt(d generatedData) string {
	var b strings.Builder

	b.WriteString(generatedHeader)
	b.WriteString("\n# Conduit\n\n")
	writeBlockquote(&b, summary)
	b.WriteString("\n")

	b.WriteString("## Pipeline configuration\n")
	fmt.Fprintf(&b, "- [Config schema (v2, latest %s)](llms-full.txt): version, pipelines, connectors, "+
		"processors, dead-letter-queue. Legacy v1 (%s) still parsed.\n\n", d.Config.LatestVersion, d.Config.LegacyVersion)

	b.WriteString("## Built-in connectors\n")
	for _, c := range d.Connectors {
		fmt.Fprintf(&b, "- %s — %s\n", c.Name, c.Summary)
	}
	fmt.Fprintf(&b, "(%d built-in connectors; full parameters in llms-full.txt)\n\n", len(d.Connectors))

	b.WriteString("## Error codes\n")
	fmt.Fprintf(&b, "- %d stable error codes, dotted `domain.name` reasons, each with a gRPC status "+
		"class. Full list with descriptions in llms-full.txt.\n\n", len(d.ErrorCodes))

	b.WriteString("## MCP tools\n")
	fmt.Fprintf(&b, "- Read: %s\n", joinToolNames(d.readTools()))
	fmt.Fprintf(&b, "- Write (require --allow-mutations): %s\n", joinToolNames(d.writeTools()))
	fmt.Fprintf(&b, "(%d tools; full schemas in llms-full.txt)\n\n", len(d.MCPTools))

	b.WriteString("## Full detail\n")
	b.WriteString("- [llms-full.txt](llms-full.txt)\n")

	return b.String()
}

// renderLLMSFullTxt renders the expanded reference: the same header/H1/
// blockquote, then every source inlined in full.
func renderLLMSFullTxt(d generatedData) string {
	var b strings.Builder

	b.WriteString(generatedHeader)
	b.WriteString("\n# Conduit\n\n")
	writeBlockquote(&b, summary)
	b.WriteString("\n")

	renderConfigSchemaSection(&b, d.Config)
	renderConnectorsSection(&b, d.Connectors)
	renderErrorCodesSection(&b, d.ErrorCodes)
	renderMCPToolsSection(&b, d.MCPTools)

	return b.String()
}

// writeBlockquote wraps text to a fixed column width and writes it as a
// Markdown blockquote (each line prefixed "> "), matching the design doc's
// illustrative formatting. Wrapping is a pure function of the (constant)
// summary text, so it is as deterministic as the constant itself.
func writeBlockquote(b *strings.Builder, text string) {
	for _, line := range wrapText(text, 96) {
		b.WriteString("> ")
		b.WriteString(line)
		b.WriteString("\n")
	}
}

// wrapText greedily wraps text to width columns, breaking only on spaces.
func wrapText(text string, width int) []string {
	words := strings.Fields(text)
	if len(words) == 0 {
		return nil
	}

	var lines []string
	line := words[0]
	for _, w := range words[1:] {
		if len(line)+1+len(w) > width {
			lines = append(lines, line)
			line = w
			continue
		}
		line += " " + w
	}
	lines = append(lines, line)
	return lines
}

func joinToolNames(tools []mcp.ToolInfo) string {
	names := make([]string, len(tools))
	for i, t := range tools {
		names[i] = t.Name
	}
	return strings.Join(names, ", ")
}

func renderConfigSchemaSection(b *strings.Builder, cfg configSchema) {
	b.WriteString("## 1. Pipeline configuration schema\n\n")

	b.WriteString("### Versions\n")
	for _, v := range cfg.Versions {
		if v == cfg.LatestVersion {
			fmt.Fprintf(b, "- %s (latest)\n", v)
		} else {
			fmt.Fprintf(b, "- %s\n", v)
		}
	}
	b.WriteString("\n")

	for _, s := range cfg.Structs {
		fmt.Fprintf(b, "### %s\n\n", s.Name)
		b.WriteString("| Field | YAML key | Go type | Required |\n")
		b.WriteString("| --- | --- | --- | --- |\n")
		for _, f := range s.Fields {
			fmt.Fprintf(b, "| %s | `%s` | `%s` | %s |\n", f.GoField, f.YAMLKey, f.GoType, yesNo(f.Required))
		}
		b.WriteString("\n")
	}

	fmt.Fprintf(b, "### Legacy v1 (%s)\n\n", cfg.LegacyVersion)
	b.WriteString("v1 pipeline configs key `pipelines`, `connectors`, and `processors` by map (" +
		"`pipelines: {<id>: {...}}`) instead of v2's ordered list, so processor and connector ordering " +
		"within a v1 file is not deterministic. v1 has no `plugin`/`condition` processor fields and no " +
		"top-level `dead-letter-queue` before 1.1. New pipelines should use v2.\n\n")
}

func renderConnectorsSection(b *strings.Builder, connectors []connectorInfo) {
	fmt.Fprintf(b, "## 2. Built-in connectors (%d)\n\n", len(connectors))

	for _, c := range connectors {
		fmt.Fprintf(b, "### %s (%s)\n\n", c.Name, c.Version)
		fmt.Fprintf(b, "%s\n\n", c.Summary)
		if c.Description != "" {
			fmt.Fprintf(b, "%s\n\n", c.Description)
		}
		fmt.Fprintf(b, "Author: %s\n\n", c.Author)

		b.WriteString("#### Source parameters\n\n")
		renderParamsTable(b, c.SourceParams)

		b.WriteString("#### Destination parameters\n\n")
		renderParamsTable(b, c.DestinationParams)
	}
}

func renderParamsTable(b *strings.Builder, params []paramInfo) {
	if len(params) == 0 {
		b.WriteString("(none)\n\n")
		return
	}

	b.WriteString("| Key | Type | Default | Required | Description | Validations |\n")
	b.WriteString("| --- | --- | --- | --- | --- | --- |\n")
	for _, p := range params {
		fmt.Fprintf(b, "| `%s` | %s | %s | %s | %s | %s |\n",
			escapeCell(p.Key), escapeCell(p.Type), escapeCell(p.Default), yesNo(p.Required),
			escapeCell(p.Description), escapeCell(strings.Join(p.Validations, ", ")))
	}
	b.WriteString("\n")
}

func renderErrorCodesSection(b *strings.Builder, codes []errorCodeInfo) {
	fmt.Fprintf(b, "## 3. Error codes (%d)\n\n", len(codes))

	b.WriteString("| Reason | gRPC status | Description |\n")
	b.WriteString("| --- | --- | --- |\n")
	for _, c := range codes {
		fmt.Fprintf(b, "| `%s` | %s | %s |\n", escapeCell(c.Reason), escapeCell(c.GRPCCode), escapeCell(c.Description))
	}
	b.WriteString("\n")
}

func renderMCPToolsSection(b *strings.Builder, tools []mcp.ToolInfo) {
	sorted := make([]mcp.ToolInfo, len(tools))
	copy(sorted, tools)
	sort.Slice(sorted, func(i, j int) bool { return sorted[i].Name < sorted[j].Name })

	fmt.Fprintf(b, "## 4. MCP tool catalog (%d)\n\n", len(sorted))

	b.WriteString("| Name | Class | Description |\n")
	b.WriteString("| --- | --- | --- |\n")
	for _, t := range sorted {
		class := "read"
		if t.Mutates {
			class = "write (requires --allow-mutations)"
		}
		fmt.Fprintf(b, "| `%s` | %s | %s |\n", escapeCell(t.Name), class, escapeCell(t.Description))
	}
	b.WriteString("\n")
}

func yesNo(b bool) string {
	if b {
		return "yes"
	}
	return "no"
}

// escapeCell makes s safe to embed as one Markdown table cell: newlines
// become spaces (a table cell cannot contain a literal line break) and
// pipe characters are escaped so they cannot be mistaken for a column
// separator.
func escapeCell(s string) string {
	s = strings.ReplaceAll(s, "\r\n", " ")
	s = strings.ReplaceAll(s, "\n", " ")
	s = strings.ReplaceAll(s, "|", "\\|")
	return strings.TrimSpace(s)
}
